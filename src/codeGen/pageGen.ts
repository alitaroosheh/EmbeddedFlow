const AUTOGEN_BANNER = `/*
 * AUTO-GENERATED by EmbeddedFlow — do not edit manually.
 * To regenerate: VSCode → Command Palette → "EmbeddedFlow: Generate C Code"
 */
`;

/** True for LVGL v9.x, false for v8.x */
export function isLvglV9(project: EmbfProject): boolean {
    return project.project.lvglVersion.startsWith("9");
}

function lvglTargetBanner(project: EmbfProject): string {
    return `/* Target LVGL ${project.project.lvglVersion} — project.lvglVersion in .embf must match the LVGL library linked in your firmware. */`;
}

// ─────────────────────────────────────────────────────────────────────────────
// Per-page header: ui_<page_id>.h
// ─────────────────────────────────────────────────────────────────────────────

export function generatePageHeader(project: EmbfProject, page: Page): string {
    const guard  = headerGuard(page.id);
    const initFn = screenInitFn(page.id);
    const scrVar = screenVar(page.id);

    const widgetExterns = flatWidgets(page).map(w =>
        `extern lv_obj_t *${widgetVar(page.id, w.id)};`
    );

    return [
        AUTOGEN_BANNER,
        `#ifndef ${guard}`,
        `#define ${guard}`,
        ``,
        `#ifdef __cplusplus`,
        `extern "C" {`,
        `#endif`,
        ``,
        lvglIncludeDirective(project),
        ``,
        `/** Screen object for page "${page.name}" */`,
        `extern lv_obj_t *${scrVar};`,
        ``,
        widgetExterns.length > 0 ? `/* Widget objects */` : "",
        ...widgetExterns,
        ``,
        `/** Create all widgets for the "${page.name}" screen. */`,
        `void ${initFn}(void);`,
        ``,
        `#ifdef __cplusplus`,
        `} /* extern "C" */`,
        `#endif`,
        ``,
        `#endif /* ${guard} */`,
        ``
    ].filter(l => l !== undefined).join("\n");
}

// ─────────────────────────────────────────────────────────────────────────────
// Per-page source: ui_<page_id>.c
// ─────────────────────────────────────────────────────────────────────────────

export function generatePageSource(
    project: EmbfProject,
    page: Page,
    opts?: { stringsApi?: boolean; textDirection?: boolean }
): string {
    const scrVar = screenVar(page.id);
    const initFn = screenInitFn(page.id);
    const v9     = isLvglV9(project);
    const stringsApi = opts?.stringsApi === true;
    const textDirection = opts?.textDirection === true;

    const gridStatics = collectPageGridDescriptors(page.id, page.components);
    const allWidgets = flatWidgets(page);
    const varDecls = allWidgets.map(w =>
        `lv_obj_t *${widgetVar(page.id, w.id)} = NULL;`
    );

    const widgetCtx = { fonts: project.fonts, styles: project.styles, stringsApi };
    const bodyLines: string[] = [];
    for (const comp of page.components) {
        bodyLines.push(...emitComponent(page.id, comp, scrVar, v9, widgetCtx));
    }

    const needsOpaHelper = anyOpacityAnimation(page.components as any[]);
    const opaHelper = needsOpaHelper ? emitOpacityHelperLines() : [];

    // Screen background color
    const bgLines: string[] = [];
    if (page.backgroundColor) {
        bgLines.push(
            `    lv_obj_set_style_bg_color(${scrVar}, lv_color_hex(0x${hexToRaw(page.backgroundColor)}), LV_PART_MAIN | LV_STATE_DEFAULT);`,
            `    lv_obj_set_style_bg_opa(${scrVar}, LV_OPA_COVER, LV_PART_MAIN | LV_STATE_DEFAULT);`
        );
    }

    // Page scroll enable/disable (optional)
    const scrollLines: string[] = [];
    if ((page as any).scrollX !== undefined || (page as any).scrollY !== undefined) {
        const sx = (page as any).scrollX === true;
        const sy = (page as any).scrollY === true;
        if (!sx && !sy) {
            scrollLines.push(`    lv_obj_clear_flag(${scrVar}, LV_OBJ_FLAG_SCROLLABLE);`);
            scrollLines.push(`    lv_obj_set_scroll_dir(${scrVar}, LV_DIR_NONE);`);
        } else {
            const dirs: string[] = [];
            if (sx) dirs.push("LV_DIR_HOR");
            if (sy) dirs.push("LV_DIR_VER");
            scrollLines.push(`    lv_obj_add_flag(${scrVar}, LV_OBJ_FLAG_SCROLLABLE);`);
            scrollLines.push(`    lv_obj_set_scroll_dir(${scrVar}, ${dirs.join(" | ")});`);
        }
    }

    // Event callbacks and page swipe handlers
    const events = collectPageEvents(project, page, stringsApi);
    const swipes = collectPageSwipes(project, page);
    const decls = [...events.decls, ...swipes.decls];
    const impls = [...events.impls, ...swipes.impls];
    const registrations = [...events.registrations, ...swipes.registrations];

    return [
        AUTOGEN_BANNER,
        `/* ui.h includes every ui_<page>.h so navigate handlers can reference other screens. */`,
        `#include "ui.h"`,
        ``,
        ...(gridStatics.length > 0 ? [`/* Grid track descriptors */`, ...gridStatics, ``] : []),
        `/* Screen variable */`,
        `lv_obj_t *${scrVar} = NULL;`,
        ``,
        allWidgets.length > 0 ? `/* Widget variables */` : "",
        ...varDecls,
        ``,
        decls.length > 0 ? `/* Event callback forward declarations */` : "",
        ...decls,
        ``,
        decls.length > 0 ? `/* Event callback implementations */` : "",
        ...impls.map(fn => fn + "\n"),
        ...opaHelper,
        `void ${initFn}(void)`,
        `{`,
        `    ${scrVar} = lv_obj_create(NULL);`,
        ...(textDirection ? [emitPageInitBaseDir(scrVar)] : []),
        ...bgLines,
        ...scrollLines,
        ``,
        ...bodyLines,
        registrations.length > 0 ? `    /* Event registrations */` : "",
        ...registrations,
        `}`,
        ``
    ].filter(l => l !== undefined).join("\n");
}

