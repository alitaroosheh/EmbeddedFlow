/** Flatten nested widget children into a single list for extern declarations. */
function flatWidgets(page: Page): Array<{ id: string }> {
    const result: Array<{ id: string }> = [];
    function walk(comps: any[]): void {
        for (const c of comps) {
            result.push({ id: c.id });
            if (c.children) walk(c.children);
        }
    }
    walk(page.components);
    return result;
}

/** Strip leading # and return raw 6-char hex, uppercase. */
function hexToRaw(hex: string): string {
    const h = hex.replace("#", "").toUpperCase();
    if (h.length === 3) {
        return h[0]+h[0]+h[1]+h[1]+h[2]+h[2];
    }
    return h.slice(0, 6).padStart(6, "0");
}
